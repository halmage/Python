class Operaciones:
    def suma(numero1, numero2):
        """
        MÉTODO QUE CALCULA LA SUMA DE LOS NUMEROS
        """
        return float(numero1) + float(numero2)

    def resta(numero1, numero2):
        """
        MÉTODO QUE CALCULA LA RESTA DE LOS NUMEROS
        """
        return float(numero1) - float(numero2)

    def multiplicacion(numero1, numero2):
        """
        MÉTODO QUE CALCULA LA MULTIPLICACION DE LOS NUMEROS
        """
        return float(numero1) * float(numero2)

    def division(numero1, numero2):
        """
        MÉTODO QUE CALCULA LA DIVISION DE LOS NUMEROS
        """
        return float(numero1) / float(numero2)
